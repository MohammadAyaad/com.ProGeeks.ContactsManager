    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        try {
            ContactDetailsEditFrame cdef = new ContactDetailsEditFrame(_contact.id);
            cdef.addWindowListener(new WindowListener() {
                @Override
                public void windowOpened(WindowEvent e) {}

                @Override
                public void windowClosing(WindowEvent e) {}

                @Override
                public void windowClosed(WindowEvent e) {
                    try {
                        updateContact();
                    } catch (SQLException ex) {
                        Logger.getLogger(ContactDetailsFrame.class.getName()).log(Level.SEVERE, null, ex);
                    } catch (InvalidEmailAddressException ex) {
                        Logger.getLogger(ContactDetailsFrame.class.getName()).log(Level.SEVERE, null, ex);
                    } catch (InvalidPhoneNumberException ex) {
                        Logger.getLogger(ContactDetailsFrame.class.getName()).log(Level.SEVERE, null, ex);
                    }
                    updateUI(_contact);
                }

                @Override
                public void windowIconified(WindowEvent e) {
                }

                @Override
                public void windowDeiconified(WindowEvent e) {
                }

                @Override
                public void windowActivated(WindowEvent e) {
                }

                @Override
                public void windowDeactivated(WindowEvent e) {
                }
            });
            
            cdef.show();
            _contact = MainClass.manager.getContactDetails(_contact.id);
            updateUI(_contact);
            // TODO add your handling code here:
        } catch (SQLException ex) {
            Logger.getLogger(ContactDetailsFrame.class.getName()).log(Level.SEVERE, null, ex);
        } catch (InvalidEmailAddressException ex) {
            Logger.getLogger(ContactDetailsFrame.class.getName()).log(Level.SEVERE, null, ex);
        } catch (InvalidPhoneNumberException ex) {
            Logger.getLogger(ContactDetailsFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

