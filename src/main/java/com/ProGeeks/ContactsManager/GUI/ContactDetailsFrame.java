    void updateUI(Contact c) {
        Name.setText(c.firstName + " " + c.lastName);
        JobTitle.setText(c.jobTitle);

        DefaultTableModel pmodel = new DefaultTableModel();
        pmodel.addColumn("Phone Number");
        for(PhoneNumber p : c.phoneNumbers) {
            pmodel.addRow(new Object[] { p.toString() });
        }
        phoneNumbersTable.setModel(pmodel);
        
        DefaultTableModel emodel = new DefaultTableModel();
        emodel.addColumn("Phone Number");
        for(Email e : c.emails) {
            emodel.addRow(new Object[] { e.toString() });
        }
        emailsTable.setModel(emodel);
    }
    
